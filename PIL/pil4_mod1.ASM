;MODULE 1
PUBLIC STR1
PUBLIC STR2
EXTRN CONCATENATE:FAR
EXTRN COMPARESTR:FAR
EXTRN CHARSPACE:FAR

.MODEL SMALL
.DATA
MSG1 DB 10,13,"ENTER A STRING :$"
STR1 DB 20H
     DB 00H
     DB 20H DUP(00)
STR2 DB 20H
     DB 00H
     DB 20H DUP(00)
DISP MACRO M1
LEA DX,M1
MOV AH,09H
INT 21H
ENDM

MSG2 DB 10,13,"OPTIONS :-"
     DB 10,13,"1.CONCATENATE"
     DB 10,13,"2.COMPARE"
     DB 10,13,"3.NUMBER OF CHARACTERS AND SPACES"
     DB 10,13,"4.EXIT$"
MSG10 DB 10,13,"FOR STRING 1 :$"
MSG11 DB 10,13,"FOR STRING 2 :$"

.CODE
MOV AX,@DATA
MOV DS,AX
MOV ES,AX

DISP MSG1               ;ACCEPT STRING 1
MOV AH,0AH
LEA DX,STR1
INT 21H

DISP MSG1               ;ACCEPT STRING2
MOV AH,0AH
LEA DX,STR2
INT 21H

UP:DISP MSG2               ;SWITCH CASE
   MOV AH,01H
   INT 21H

CMP AL,31H
JE FIRST
CMP AL,32H
JE SECOND
CMP AL,33H
JE THIRD
CMP AL,34H
JE FOURTH

FIRST : CALL CONCATENATE
JMP UP
SECOND: CALL COMPARESTR
JMP UP
THIRD:DISP MSG10
LEA SI,STR1
CALL CHARSPACE
DISP MSG11
LEA SI,STR2
CALL CHARSPACE
JMP UP
FOURTH: MOV AH,4CH
       INT 21H

END
